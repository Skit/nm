from bot.src.app.server.db import Db, StoreException


class Connection(Db):
    def __init__(self):
        try:
            super().__init__()
            self.conn = Db.get_connection(self)
        except Exception as e:
            raise StoreException(*e.args)
        self._complete = False

    def __enter__(self):
        return self

    def __exit__(self, type_, value, traceback):
        self.close()

    def complete(self):
        self._complete = True

    def close(self):
        if self.conn:
            try:
                if self._complete:
                    self.conn.commit()
                else:
                    self.conn.rollback()
            except Exception as e:
                raise StoreException(*e.args)
            finally:
                try:
                    self.conn.close()
                except Exception as e:
                    raise StoreException(*e.args)
